/*
 * spectral_qldpc.c — Shor's-Period-Finding Approach to Quantum Codes
 *
 * The QFT (quantum Fourier transform) — the core of Shor's algorithm —
 * diagonalizes circulant matrices.  For a Generalized Bicycle code defined
 * by polynomials a(x), b(x) over F_2[x]/(x^L+1):
 *
 *   rank(circulant from c(x)) = L − deg(gcd(c(x), x^L+1))
 *
 * This replaces O(L³) Gaussian elimination with O(L²) polynomial gcd.
 * The quantum analogue: a single QFT over Z_L reveals the full spectral
 * decomposition — Shor's period-finding directly outputs the gcd degree.
 *
 * We use this spectral method to search 2^(2L) polynomial pairs at scale —
 * computing K analytically in microseconds rather than milliseconds.
 * Distance lower bounds come from the classical dual via gcd properties.
 *
 * The QFT connection: for c ∈ F_2[x]/(x^L+1), the QFT maps x → ω^k
 * where ω is a primitive L-th root of unity.  The eigenvalues of the
 * circulant are c(ω^k) ∈ {0,1} in the appropriate extension field.
 * rank = #{k : c(ω^k) ≠ 0}.  Equivalently: rank = L − multiplicity of
 * the root α=1 in gcd(c(x), x^L+1) over the algebraic closure.
 *
 * Build:
 *   gcc -std=gnu11 -O3 -march=native -I. -o spectral_qldpc spectral_qldpc.c qubit_triality.c -lm
 *   ./spectral_qldpc               # search L up to 63, 10M samples
 */
